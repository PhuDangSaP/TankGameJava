package data;

import java.io.Serializable;

/**
 *
 * @author USER
 */
public class GameData implements Serializable {
    private int level;
    private int score;

    public GameData(int level, int score) {
        this.level = level;
        this.score = score;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
